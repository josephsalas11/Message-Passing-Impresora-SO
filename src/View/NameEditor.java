    private void editorNameBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editorNameBtnActionPerformed
       if (editorName.getText().equals(""))
               {
                 JOptionPane.showMessageDialog(null, "ingrese un nombre v√°lido para el editor"
                    , "ERROR", JOptionPane.ERROR_MESSAGE);  
               }
       else
       {
           WordUI wordUI = new WordUI(fm.getProcessCounter(), editorName.getText()); 
           
           //Si se creo el proceso, mostrar ventana
            if(wordUI.getProcess() != null){
                wordUI.setVisible(true);
                this.dispose();
                fm.setProcessCounter(fm.getProcessCounter()+1);
            }
            //Si no, eliminar WordUI y mostrar mensaje de error
            else{
                JOptionPane.showMessageDialog(null, "El editor con el nombre "+editorName.getText()+" ya fue tomado"
                        , "ERROR", JOptionPane.ERROR_MESSAGE);
                wordUI.dispose();
            }         
       }
    }//GEN-LAST:event_editorNameBtnActionPerformed

